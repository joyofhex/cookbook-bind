# frozen_string_literal: true
require 'spec_helper'

describe 'bind_test::chroot' do
  context 'on CentOS 6' do
    let(:chef_run) do
      ChefSpec::SoloRunner.new(
        platform: 'centos', version: '6.9', step_into: %w(
          bind_service bind_config bind_acl bind_primary_zone bind_primary_zone_template bind_secondary_zone bind_forward_zone
        )
      ).converge(described_recipe)
    end

    it 'uses bind_primary_zone resource' do
      expect(chef_run).to create_bind_primary_zone('example.com')
    end

    it 'uses bind_primary_zone_template resource' do
      expect(chef_run).to create_bind_primary_zone_template('sub.example.com')
    end

    it 'uses bind_secondary_zone resource' do
      expect(chef_run).to create_bind_secondary_zone('secondary.example.com')
    end

    it 'uses bind_forward_zone resource' do
      expect(chef_run).to create_bind_forward_zone('forward.example.com')
    end

    %w(bind-chroot bind-utils bind-libs).each do |bind_package|
      it "installs package #{bind_package}" do
        expect(chef_run).to install_package(bind_package)
      end
    end

    it 'renders file /var/named/primary/db.sub.example.com' do
      expect(chef_run).to render_file('/var/named/primary/db.sub.example.com')
    end

    it 'notifies service[named]' do
      config_template = chef_run.template('/etc/named/named.options')
      expect(config_template).to notify('bind_service[default]').to(:restart)
    end

    %w(named.options named.rfc1912.zones primary.zones secondary.zones forward.zones).each do |named_file|
      it "renders file #{::File.join('/etc/named', named_file)}" do
        expect(chef_run).to render_file(::File.join('/etc/named', named_file))
      end
    end

    it 'will render secondary with no options' do
      stanza = <<~EOF
        # Generated by Chef for fauxhai.local
        # Local modifications will be overwritten.

        zone "." IN {
          type hint;
          file "named.ca";
        };

        include "/etc/named/named.rfc1912.zones";
        include "/etc/named/named.options";
        include "/etc/named/primary.zones";
        include "/etc/named/secondary.zones";
        include "/etc/named/forward.zones";
      EOF
      expect(chef_run).to render_file('/etc/named.conf').with_content { |content|
        expect(content).to include stanza
      }
    end
  end

  context 'on CentOS 7' do
    let(:chef_run) do
      ChefSpec::SoloRunner.new(
        platform: 'centos', version: '7.3.1611', step_into: %w(
          bind_service bind_config bind_acl
        )
      ).converge(described_recipe)
    end

    %w(bind-chroot bind-utils bind-libs).each do |bind_package|
      it "installs package #{bind_package}" do
        expect(chef_run).to install_package(bind_package)
      end
    end

    it 'creates /var/named/chroot with mode 750 and owner bind' do
      expect(chef_run).to create_directory('/var/named/chroot').with(
        mode: '0750',
        user: 'root',
        group: 'named'
      )
    end

    %w(dev etc var var/log var/run).each do |subdir|
      it "creates #{::File.join('/var/named/chroot', subdir)} with mode 750 and owner named" do
        expect(chef_run).to create_directory(::File.join('/var/named/chroot', subdir)).with(
          mode: '0750',
          user: 'named'
        )
      end
    end

    %w(named.options named.rfc1912.zones primary.zones secondary.zones forward.zones).each do |named_file|
      it "renders file #{::File.join('/etc/named', named_file)}" do
        expect(chef_run).to render_file(::File.join('/etc/named', named_file))
      end
    end

    it 'renders file /etc/named.conf with included files' do
      expect(chef_run).to render_file('/etc/named.conf').with_content(%r{include "/etc/named/named.options"})
      expect(chef_run).to render_file('/etc/named.conf').with_content(%r{include "/etc/named/named.rfc1912.zones"})
      expect(chef_run).to render_file('/etc/named.conf').with_content(%r{include "/etc/named/primary.zones"})
      expect(chef_run).to render_file('/etc/named.conf').with_content(%r{include "/etc/named/secondary.zones"})
      expect(chef_run).to render_file('/etc/named.conf').with_content(%r{include "/etc/named/forward.zones"})
    end

    %w(named.empty named.loopback named.localhost named.ca).each do |var_file|
      it "it creates cookbook file #{::File.join('/var/named', var_file)}" do
        expect(chef_run).to create_cookbook_file(::File.join('/var/named', var_file))
      end
    end

    %w(data primary secondary).each do |subdir|
      it "creates subdirectory #{::File.join('/var/named', subdir)}" do
        expect(chef_run).to create_directory(::File.join('/var/named', subdir))
      end
    end

    it 'executes generate_rndc_key' do
      expect(chef_run).to run_execute('generate_rndc_key')
    end

    it 'notifies service[named-chroot]' do
      config_template = chef_run.template('/etc/named/named.options')
      expect(config_template).to notify('bind_service[default]').to(:restart)
    end
  end

  context 'on Ubuntu 14.04' do
    let(:chef_run) do
      ChefSpec::SoloRunner.new(
        platform: 'ubuntu', version: '14.04', step_into: %w(
          bind_service bind_config bind_acl
        )
      ).converge(described_recipe)
    end

    %w(bind9 bind9-host bind9utils).each do |bind_package|
      it "installs package #{bind_package}" do
        expect(chef_run).to install_package(bind_package)
      end
    end

    it 'creates /var/bind9/chroot with mode 750 and owner bind' do
      expect(chef_run).to create_directory('/var/bind9/chroot').with(
        mode: '0750',
        user: 'root',
        group: 'bind'
      )
    end

    %w(dev etc var var/log var/run).each do |subdir|
      it "creates #{::File.join('/var/bind9/chroot', subdir)} with mode 750 and owner bind" do
        expect(chef_run).to create_directory(::File.join('/var/bind9/chroot', subdir)).with(
          mode: '0750',
          user: 'bind'
        )
      end
    end

    it 'creates /var/bind9/chroot/var/cache/bind with mode 750 and owner bind' do
      expect(chef_run).to create_directory('/var/bind9/chroot/var/cache/bind').with(
        mode: '0750',
        user: 'bind'
      )
    end

    it 'creates /var/bind9/chroot/var/cache/bind/dynamic with mode 750 and owner bind' do
      expect(chef_run).to create_directory('/var/bind9/chroot/var/cache/bind/dynamic').with(
        mode: '0750',
        user: 'bind'
      )
    end

    it 'creates /var/bind9/chroot/etc/bind with mode 750 and owner bind' do
      expect(chef_run).to create_directory('/var/bind9/chroot/etc/bind').with(
        mode: '0750',
        user: 'bind'
      )
    end

    %w(named.options named.rfc1912.zones primary.zones secondary.zones forward.zones).each do |named_file|
      it "renders file #{::File.join('/var/bind9/chroot/etc/bind', named_file)}" do
        expect(chef_run).to render_file(::File.join('/var/bind9/chroot/etc/bind', named_file))
      end
    end

    it 'renders file /var/bind9/chroot/etc/bind/named.conf with included files' do
      expect(chef_run).to render_file('/var/bind9/chroot/etc/bind/named.conf').with_content(%r{include "/etc/bind/named.options"})
      expect(chef_run).to render_file('/var/bind9/chroot/etc/bind/named.conf').with_content(%r{include "/etc/bind/named.rfc1912.zones"})
      expect(chef_run).to render_file('/var/bind9/chroot/etc/bind/named.conf').with_content(%r{include "/etc/bind/primary.zones"})
      expect(chef_run).to render_file('/var/bind9/chroot/etc/bind/named.conf').with_content(%r{include "/etc/bind/secondary.zones"})
      expect(chef_run).to render_file('/var/bind9/chroot/etc/bind/named.conf').with_content(%r{include "/etc/bind/forward.zones"})
    end

    %w(named.empty named.loopback named.localhost named.ca).each do |var_file|
      it "it creates cookbook file /var/bind9/chroot/var/cache/bind/#{var_file}" do
        expect(chef_run).to create_cookbook_file("/var/bind9/chroot/var/cache/bind/#{var_file}")
      end
    end

    %w(data primary secondary).each do |subdir|
      it "creates subdirectory /var/bind9/chroot/var/cache/bind/#{subdir}" do
        expect(chef_run).to create_directory("/var/bind9/chroot/var/cache/bind/#{subdir}")
      end
    end

    it 'renders file /etc/default/bind9' do
      expect(chef_run).to render_file('/etc/default/bind9')
    end

    it 'renders file /etc/init.d/bind9' do
      expect(chef_run).to render_file('/etc/init.d/bind9')
    end

    %w(null random urandom).each do |d|
      it "executes mknod #{::File.join('/var/bind9/chroot/dev', d)}" do
        expect(chef_run).to run_execute("mknod_#{d}")
      end

      it "doesn't execute chmod on #{::File.join('/var/bind9/chroot/dev', d)}" do
        expect(chef_run).to_not run_execute("chmod_dev_#{d}")
      end

      it "doesn't execute chgrp on #{::File.join('/var/bind9/chroot/dev', d)}" do
        expect(chef_run).to_not run_execute("chgrp_dev_#{d}")
      end
    end

    it 'executes generate_rndc_key' do
      expect(chef_run).to run_execute('generate_rndc_key')
    end

    it 'notifies service[bind]' do
      config_template = chef_run.template('/var/bind9/chroot/etc/bind/named.options')
      expect(config_template).to notify('bind_service[default]').to(:restart)
    end
  end
end
